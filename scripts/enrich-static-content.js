function main() {
  const files = collectHtmlFiles();
  const changed = [];

  for (const file of files) {
    const original = fs.readFileSync(file, "utf8");
    let next = normalizeTrustCopy(original);
    const content = resolveContent(file, next);
    if (content) {
      next = upsertStaticContent(next, content);
    }
    if (next !== original) {
      fs.writeFileSync(file, next);
      changed.push(path.relative(ROOT, file).replace(/\\/g, "/"));
    }
  }

  console.log(`static content enriched: ${changed.length ? changed.join(", ") : "no changes"}`);
}

function collectHtmlFiles() {
  const files = [];
  walk(ROOT, (file) => {
    if (!file.endsWith(".html")) return;
    if (/\s\(\d+\)\.html$/i.test(path.basename(file))) return;
    files.push(file);
  });
  return files.sort();
}

function walk(dir, onFile) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      if ([".git", ".browser-profile", ".cloudflare-dist", ".cloudflare-dist-smoke", ".wrangler", "node_modules"].includes(entry.name)) {
        continue;
      }
      walk(fullPath, onFile);
    } else {
      onFile(fullPath);
    }
  }
}

