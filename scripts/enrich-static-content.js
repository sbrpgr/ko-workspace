function upsertStaticContent(html, content) {
  const clean = removeExistingPanel(html);
  const footerPattern = /\n(\s*<footer class="site-footer">)/;
  if (!footerPattern.test(clean)) {
    throw new Error(`Could not find footer insertion point for ${content.id}`);
  }
  return clean.replace(footerPattern, `\n${renderPanel(content)}\n\n$1`);
}

function removeExistingPanel(html) {
  const pattern = new RegExp(`\\n?\\s*${escapeRegExp(START)}[\\s\\S]*?${escapeRegExp(END)}\\s*\\n?`, "g");
  return html.replace(pattern, "\n");
}

function renderPanel(content) {
  const id = `staticContentTitle-${content.id}`;
  return `          ${START}
          <section class="panel static-content-panel" aria-labelledby="${escapeAttr(id)}">
            <div class="static-content-heading">
              <p class="eyebrow">${escapeHtml(content.eyebrow)}</p>
              <h2 id="${escapeAttr(id)}">${escapeHtml(content.heading)}</h2>
              <p>${escapeHtml(content.lead)}</p>
            </div>
            <div class="static-story-grid">
${content.scenarios.map(renderScenario).join("\n")}
            </div>
            <p class="static-content-value">${escapeHtml(content.value)}</p>
            <ul class="static-note-list">
${content.notes.map((note) => `              <li>${escapeHtml(note)}</li>`).join("\n")}
            </ul>
          </section>
          ${END}`;
}

function renderScenario(item) {
  return `              <article>
                <h3>${escapeHtml(item.title)}</h3>
                <p>${escapeHtml(item.text)}</p>
              </article>`;
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll("\"", "&quot;")
    .replaceAll("'", "&#39;");
}

function escapeAttr(value) {
  return escapeHtml(value);
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

